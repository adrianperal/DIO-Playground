function showAge (name, age){
    return `${name}'s age is ${age} years old.`;
}

function showCity (name, city){
    return `${name}'s hometown is ${city} city.`;
}

function showHobby (name, hobby){
    return `${name}'s hobby is ${hobby}, and loves doing that!.`;
}

export{
    showAge,
    showCity,
    showHobby
}